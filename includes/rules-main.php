    <h2>ПРАВИЛА РЕЦЕНЗИРОВАНИЯ</h2>
    <p>Этические принципы работы рецензента</p>
    <p>Редакционная коллегия журнала «Экономические и гуманитарные исследования регионов» направляет все присланные статьи на рецензирование двум рецензентам. Рецензирование проводится по «слепому методу», рецензенты не знают имени автора. Рецензенты обязаны давать объективную и аргументированную оценку изложенным результатам исследования. Персональная критика автора не допускается. Неопубликованные данные, полученные из представленных к рассмотрению рукописей, рецензентам запрещено использовать для личных целей.</p>
    <p>Рецензент, который не обладает, по его мнению, достаточной квалификацией для оценки рукописи, не может быть объективным по каким-то причинам, должен сообщить об этом главному редактору журнала «Экономические и гуманитарные исследования регионов» с просьбой исключить его из процесса рецензирования данной рукописи. Рецензенты должны избегать конфликта интересов в отношении авторов рецензируемой статьи, описанных в ней исследований или организации, которая финансировала эти исследования.</p>

    <p>Результатом рассмотрения статьи рецензентами является вывод о возможности ее публикации – в представленном виде, с доработкой, после принципиальной переработки (с повторным рассмотрением) или об ее отклонении. Этот вывод утверждается редакционной коллегией и редакционным советом журнала «Экономические и гуманитарные исследования регионов». По результатам рецензирования автору ( в случае необходимости доработать статью) направляется отзыв с указанием недоработок и времени, в течение которого их необходимо исправить.</p>
    <p>Порядок рецензирования статей</p>
    <p>Все статьи, поступившие в журнал «Экономические и гуманитарные исследования регионов», проверяются на плагиат (оригинальность должна составлять не менее 80%), рецензируются членами редакционной коллегии «Экономические и гуманитарные исследования регионов» - докторами наук по соответствующей специальности, далее подвергаются корректорской правке. Все правки согласовываются с автором статьи.</p>

    <p>При получении статьи специалист журнала (технически или ответственный редактор) определяет соответствие статьи профилю журнала, требованиям к оформлению и направляет статью на рецензирование двум членам редакционной коллегии или редакционного совета журнала «Экономические и гуманитарные исследования регионов» — докторам наук, имеющим наиболее близкую к теме статьи научную специализацию.</p>
    <p>Срок рецензирования статьи - 10 дней.</p>
