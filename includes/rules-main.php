    <h2>REVIEW RULES</h2>
    <p>Ethical principles of work of the reviewer</p>
    <p>The editorial board of the journal "Economic and Humanitarian Studies of the Regions" sends all submitted articles for review to two reviewers. Reviewing is carried out according to the "blind method", the reviewers do not know the name of the author. Reviewers are obliged to give an objective and reasoned assessment of the stated results of the study. Personal criticism of the author is not allowed. Unpublished data obtained from submitted manuscripts may not be used by reviewers for personal purposes.</p>
    <p>A reviewer who, in his opinion, does not have sufficient qualifications to evaluate the manuscript, cannot be objective for some reason, should inform the editor-in-chief of the journal Economic and Humanitarian Research of Regions about this with a request to exclude him from the review process of this manuscript. Reviewers should avoid conflicts of interest with respect to the authors of the peer-reviewed article, the studies described in it, or the organization that funded these studies.</p>

    <p>The result of reviewing the article by the reviewers is the conclusion about the possibility of its publication - in the presented form, with revision, after fundamental revision (with re-review) or its rejection. This conclusion is approved by the editorial board and the editorial board of the journal "Economic and Humanitarian Studies of the Regions". Based on the results of the review, the author (if necessary, modify the article) is sent a review indicating the shortcomings and the time during which they need to be corrected.</p>
    <p>The procedure for reviewing articles</p>
    <p>All articles submitted to the journal "Economic and Humanitarian Research of Regions" are checked for plagiarism (originality must be at least 80%), reviewed by members of the editorial board of "Economic and Humanitarian Research of Regions" - doctors of science in the relevant specialty, then subject to proofreading. All edits are agreed with the author of the article.</p>

    <p>Upon receipt of an article, a journal specialist (technically or a responsible editor) determines whether the article complies with the journal’s profile, formatting requirements, and sends the article for review to two members of the editorial board or editorial board of the journal “Economic and Humanitarian Research of Regions” — doctors of science who have the article closest to the topic scientific specialization.</p>
    <p>Article review period - 10 days.</p>
